DEFAULT_OUTPUT = Path(__file__).resolve().parent / "dashboard.html"
FRONTIER_TRADE_IDS = {"T026", "T027", "T028", "T033"}

STATUS_COLORS = {
    "COMPLIANT": "#2f7d4f",
    "NONCOMPLIANT": "#b83232",
    "CONDITIONAL": "#b7791f",
    "NOT_APPLICABLE": "#586174",
}

STATUS_LABEL_COLORS = {
    "COMPLIANT": "#e7f5ec",
    "NONCOMPLIANT": "#fde8e8",
    "CONDITIONAL": "#fff4d6",
    "NOT_APPLICABLE": "#edf0f5",
}

STATUS_ORDER = ["COMPLIANT", "NONCOMPLIANT", "CONDITIONAL", "NOT_APPLICABLE", "UNKNOWN"]


def load_report(path: Path) -> list[dict[str, Any]]:
    """Load the compliance report generated by run_compliance_check.py."""

    with path.open("r", encoding="utf-8") as handle:
        data = json.load(handle)
    if not isinstance(data, list):
        raise ValueError(f"{path} must contain a JSON array")
    return data


def regimes_in_report(rows: list[dict[str, Any]]) -> list[str]:
    """Return regime names in first-seen order."""

    regimes: list[str] = []
    for row in rows:
        for regime in row.get("regime_results", {}):
            if regime not in regimes:
                regimes.append(regime)
    return regimes


def status_badge(status: str) -> str:
    """Render a compact status badge."""

    bg = STATUS_LABEL_COLORS.get(status, "#f1f5f9")
    fg = STATUS_COLORS.get(status, "#1f2937")
    return (
        f'<span class="status-badge" style="background:{bg};color:{fg};">'
        f"{html.escape(status)}</span>"
    )


def frontier_rows(rows: list[dict[str, Any]]) -> list[dict[str, Any]]:
    """Return the four trades used for the Part 4 classification frontier."""

    return [row for row in rows if row.get("trade_id") in FRONTIER_TRADE_IDS]


def money_label(row: dict[str, Any]) -> str:
    """Format notional amount and currency from classified fields."""

    fields = row.get("classified_fields", {})
    amount = fields.get("notional_amount")
    currency = fields.get("notional_currency") or ""
    if isinstance(amount, (int, float)):
        return f"{amount:,.0f} {currency}".strip()
    return html.escape(str(amount or "N/A"))


def compact_list(items: list[str], limit: int = 8) -> str:
    """Format a compact comma-separated list with an overflow marker."""

    visible = items[:limit]
    text = ", ".join(visible)
    if len(items) > limit:
        text += f" +{len(items) - limit} more"
    return text


def render_part4_overview(rows: list[dict[str, Any]], regimes: list[str]) -> str:
    """Render headline metrics for the event-contract frontier."""

    frontier = frontier_rows(rows)
    status_counts = Counter(
        result.get("status", "UNKNOWN")
        for row in rows
        for result in row.get("regime_results", {}).values()
    )
    source_counts = Counter(row.get("source") or "unknown" for row in rows)
    no_taxonomy = sum(
        row.get("classification_flag") == "NOVEL_INSTRUMENT_NO_TAXONOMY"
        for row in frontier
    )
    cftc_conditional = sum(
        row.get("regime_results", {}).get("CFTC", {}).get("status") == "CONDITIONAL"
        for row in frontier
    )
    emir_outside = sum(
        row.get("regime_results", {}).get("EMIR", {}).get("status") == "NOT_APPLICABLE"
        for row in frontier
    )

    metrics = [
        ("Trades Analysed", len(rows), "All loaded from the JSON report"),
        ("Regime Decisions", sum(status_counts.values()), ", ".join(regimes)),
        ("Frontier Trades", len(frontier), "T026-T028 and T033"),
        ("No DSB Taxonomy", no_taxonomy, "No matched UPI template"),
        ("Conditional CFTC", cftc_conditional, "Event-contract classification boundary"),
        ("Outside EMIR", emir_outside, "Not treated as EMIR OTC derivatives here"),
    ]
    cards = "".join(
        f"""
        <div class="metric-card">
          <span>{html.escape(label)}</span>
          <strong>{value}</strong>
          <small>{html.escape(note)}</small>
        </div>
        """
        for label, value, note in metrics
    )

    return f"""
    <section class="frontier-overview">
      <div class="section-kicker">Event-Contract Focus</div>
      <h2>Classification Frontier for Event Contracts</h2>
      <p>
        This dashboard separates ordinary reporting errors from the harder
        classification question: whether prediction and event contracts fit existing OTC
        derivative reporting taxonomies. The four highlighted trades are analysed
        directly from {html.escape(', '.join(regimes))} results in the JSON report.
        Source mix: {html.escape(', '.join(f'{k}: {v}' for k, v in source_counts.items()))}.
      </p>
      <div class="metric-grid">{cards}</div>
    </section>
    """


def render_heatmap(rows: list[dict[str, Any]], regimes: list[str]) -> str:
    """Render portfolio compliance status by trade and regime."""

    body_rows = []
    for row in rows:
        row_class = ' class="frontier-row"' if row.get("trade_id") in FRONTIER_TRADE_IDS else ""
        cells = [
            f"<th>{html.escape(row['trade_id'])}</th>",
            f"<td>{html.escape(row.get('asset_class') or '')}</td>",
        ]
        for regime in regimes:
            result = row.get("regime_results", {}).get(regime, {})
            status = result.get("status", "UNKNOWN")
            color = STATUS_COLORS.get(status, "#64748b")
            cells.append(
                f'<td class="heat-cell" style="background:{color};">'
                f"{html.escape(status)}</td>"
            )
        body_rows.append(f"<tr{row_class}>{''.join(cells)}</tr>")

    regime_headers = "".join(f"<th>{html.escape(regime)}</th>" for regime in regimes)
    legend = "".join(
        f"""
        <span class="legend-item">
          <i style="background:{STATUS_COLORS.get(status, '#64748b')}"></i>
          {html.escape(status)}
        </span>
        """
        for status in STATUS_ORDER
        if any(
            result.get("status", "UNKNOWN") == status
            for row in rows
            for result in row.get("regime_results", {}).values()
        )
    )
    return f"""
    <section>
      <h2>Portfolio Compliance Heatmap</h2>
      <div class="legend">{legend}</div>
      <div class="scroll-table">
        <table class="heatmap">
          <thead>
            <tr><th>Trade</th><th>Asset Class</th>{regime_headers}</tr>
          </thead>
          <tbody>
            {''.join(body_rows)}
          </tbody>
        </table>
      </div>
    </section>
    """


def collect_messages(row: dict[str, Any]) -> list[str]:
    """Collect all errors and warnings attached to one report row."""

    messages: list[str] = []
    messages.extend(row.get("parse_errors", []))
    messages.extend(row.get("upi_errors", []))
    messages.extend(row.get("upi_warnings", []))
    for result in row.get("regime_results", {}).values():
        messages.extend(result.get("errors", []))
        messages.extend(result.get("warnings", []))
    return [message for message in messages if message]


def extract_field_name(message: str) -> str:
    """Extract a stable field-like label from an error or warning message."""

    patterns = [
        r"missing(?: EMIR)? required field: ([A-Za-z0-9_]+)",
        r"([A-Za-z0-9_]+)=.* is not in codeset",
        r"([A-Za-z0-9_]+) failed ISO",
        r"([A-Za-z0-9_]+) must ",
        r"([A-Za-z0-9_]+) is missing",
    ]
    for pattern in patterns:
        match = re.search(pattern, message)
        if match:
            return match.group(1)

    lowered = message.lower()
    if "uti namespace" in lowered or "uti suffix" in lowered:
        return "uti"
    if "libor" in lowered:
        return "reference_rate"
    if "parse issue" in lowered or "timestamp" in lowered:
        return "execution_timestamp"
    return "other"


def render_error_frequency(rows: list[dict[str, Any]]) -> str:
    """Render horizontal bars for the most common failing fields."""

    counts = Counter()
    affected_trades: dict[str, set[str]] = defaultdict(set)
    for row in rows:
        for message in collect_messages(row):
            field = extract_field_name(message)
            counts[field] += 1
            affected_trades[field].add(row.get("trade_id", "UNKNOWN"))

    if not counts:
        return "<section><h2>Error Frequency</h2><p>No errors or warnings found.</p></section>"

    max_count = max(counts.values())
    bars = []
    for field, count in counts.most_common(12):
        width = max(6, round((count / max_count) * 100))
        bars.append(
            f"""
            <div class="bar-row">
              <div class="bar-label">{html.escape(field)}</div>
              <div class="bar-track">
                <div class="bar-fill" style="width:{width}%"></div>
              </div>
              <div class="bar-value">{count}</div>
              <div class="bar-trades">Affected trades: {html.escape(compact_list(sorted(affected_trades[field]), 10))}</div>
            </div>
            """
        )

    return f"""
    <section>
      <h2>Error Frequency</h2>
      <div class="bar-chart">{''.join(bars)}</div>
    </section>
    """


def compliance_rate(status_counts: Counter[str]) -> float:
    """Return compliant trades divided by all status-bearing trades."""

    total = sum(status_counts.values())
    if total == 0:
        return 0.0
    return status_counts.get("COMPLIANT", 0) / total


def render_asset_breakdown(rows: list[dict[str, Any]], regimes: list[str]) -> str:
    """Render status mix by asset class and regime."""

    grouped: dict[str, dict[str, Counter[str]]] = defaultdict(lambda: defaultdict(Counter))
    for row in rows:
        asset_class = row.get("asset_class") or "Unknown"
        for regime in regimes:
            status = row.get("regime_results", {}).get(regime, {}).get("status", "UNKNOWN")
            grouped[asset_class][regime][status] += 1

    body_rows = []
    for asset_class in sorted(grouped):
        cells = [f"<th>{html.escape(asset_class)}</th>"]
        for regime in regimes:
            counts = grouped[asset_class][regime]
            total = sum(counts.values())
            rate = round(compliance_rate(counts) * 100)
            counts_text = ", ".join(
                f"{status}: {counts[status]}"
                for status in STATUS_ORDER
                if counts.get(status)
            )
            segments = "".join(
                f"""
                <span
                  style="width:{(counts[status] / total) * 100 if total else 0:.2f}%;
                         background:{STATUS_COLORS.get(status, '#64748b')};"
                  title="{html.escape(status)}: {counts[status]}"
                ></span>
                """
                for status in STATUS_ORDER
                if counts.get(status)
            )
            cells.append(
                f"""
                <td>
                  <div class="asset-cell">
                    <div class="stack-track">{segments}</div>
                    <strong>{rate}% compliant</strong>
                    <small>{html.escape(counts_text)}</small>
                  </div>
                </td>
                """
            )
        body_rows.append(f"<tr>{''.join(cells)}</tr>")

    headers = "".join(f"<th>{html.escape(regime)}</th>" for regime in regimes)
    return f"""
    <section>
      <h2>Asset Class Breakdown</h2>
      <table>
        <thead><tr><th>Asset Class</th>{headers}</tr></thead>
        <tbody>{''.join(body_rows)}</tbody>
      </table>
    </section>
    """


