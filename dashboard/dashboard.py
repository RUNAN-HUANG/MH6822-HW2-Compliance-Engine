def parse_args() -> argparse.Namespace:
    """Parse dashboard generation arguments."""

    parser = argparse.ArgumentParser(description="Generate the compliance dashboard HTML.")
    parser.add_argument(
        "--input",
        default=str(DEFAULT_REPORT),
        help="Path to the compliance_report.json file.",
    )
    parser.add_argument(
        "--output",
        default=str(DEFAULT_OUTPUT),
        help="Path for the generated dashboard HTML.",
    )
    return parser.parse_args()


def main() -> None:
    """Generate the dashboard file."""

    args = parse_args()
    input_path = Path(args.input)
    output_path = Path(args.output)
    rows = load_report(input_path)
    html_text = render_dashboard(rows, input_path)

    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(html_text, encoding="utf-8")
    print(f"Wrote dashboard for {len(rows)} trades to {output_path}")


if __name__ == "__main__":
    main()
