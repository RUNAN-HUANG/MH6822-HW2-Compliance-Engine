def render_interpretation(rows: list[dict[str, Any]]) -> str:
    """Render the required written interpretation paragraphs."""

    regimes = regimes_in_report(rows)
    status_counts = Counter(
        result.get("status", "UNKNOWN")
        for row in rows
        for result in row.get("regime_results", {}).values()
    )
    noncompliant_trades = sorted(
        {
            row["trade_id"]
            for row in rows
            for result in row.get("regime_results", {}).values()
            if result.get("status") == "NONCOMPLIANT"
        }
    )
    message_counts = Counter()
    for row in rows:
        for message in collect_messages(row):
            message_counts[extract_field_name(message)] += 1

    event_trades = [row["trade_id"] for row in frontier_rows(rows)]
    event_statuses = Counter(
        result.get("status", "UNKNOWN")
        for row in rows
        if row.get("trade_id") in FRONTIER_TRADE_IDS
        for result in row.get("regime_results", {}).values()
    )
    fully_compliant_controls = [
        row["trade_id"]
        for row in rows
        if row.get("source") == "additional"
        and all(
            row.get("regime_results", {}).get(regime, {}).get("status") == "COMPLIANT"
            for regime in regimes
        )
    ]
    designed_exceptions = [
        row["trade_id"]
        for row in rows
        if row.get("source") == "additional"
        and any(
            row.get("regime_results", {}).get(regime, {}).get("status")
            in {"NONCOMPLIANT", "CONDITIONAL", "NOT_APPLICABLE"}
            for regime in regimes
        )
    ]

    top_fields = ", ".join(
        f"{field} ({count})" for field, count in message_counts.most_common(4)
    )
    noncompliant_preview = ", ".join(noncompliant_trades[:18])
    if len(noncompliant_trades) > 18:
        noncompliant_preview += " and others"
    event_status_text = ", ".join(
        f"{status}: {count}" for status, count in event_statuses.items()
    )
    compliant_count = status_counts.get("COMPLIANT", 0)
    noncompliant_count = status_counts.get("NONCOMPLIANT", 0)
    conditional_count = status_counts.get("CONDITIONAL", 0)
    not_applicable_count = status_counts.get("NOT_APPLICABLE", 0)
    frontier_platforms = ", ".join(
        sorted(
            {
                row.get("classified_fields", {}).get("platform")
                for row in frontier_rows(rows)
                if row.get("classified_fields", {}).get("platform")
            }
        )
    )
    frontier_use_cases = ", ".join(
        f"{row.get('trade_id')} ({row.get('use_case')})"
        for row in frontier_rows(rows)
    )
    asset_counts = Counter(row.get("asset_class") or "Unknown" for row in rows)
    asset_mix = ", ".join(
        f"{asset}: {count}" for asset, count in asset_counts.most_common()
    )

    return f"""
    <section class="interpretation">
      <h2>Interpretation</h2>
      <p>
        The dashboard is generated from a single source file,
        <strong>outputs/compliance_report.json</strong>, which contains {len(rows)}
        trades and {sum(status_counts.values())} regime-level decisions across
        {html.escape(', '.join(regimes))}. The portfolio heatmap shows a strongly
        exception-heavy reporting book: {noncompliant_count} regime outcomes are
        non-compliant, compared with {compliant_count} compliant outcomes,
        {conditional_count} conditional outcomes, and {not_applicable_count}
        not-applicable outcomes. This matters because the failures are not confined
        to one isolated trade; non-compliant trades include
        {html.escape(noncompliant_preview)}, indicating a portfolio-level control
        problem rather than a single booking mistake.
      </p>
      <p>
        The error-frequency chart narrows the operational problem to repeatable
        data-quality checks. The most frequent message groups are
        {html.escape(top_fields or 'none')}. These are exactly the types of fields
        that a pre-submission validation layer should catch: counterparty LEI
        format and checksum controls, mandatory collateral and margin fields, UTI
        consistency, and reference-rate hygiene. In other words, the dashboard does
        not merely count failed trades; it points to the specific controls that
        would reduce the greatest number of reporting breaks.
      </p>
      <p>
        The asset-class breakdown gives useful context for the classification question.
        The source data covers {html.escape(asset_mix)}, so the event-contract
        trades are being compared against a mixed OTC derivative portfolio rather
        than viewed in isolation. Conventional asset classes mainly fail because
        required reporting fields or identifiers are invalid. By contrast, the
        event-contract records fail to map into an ANNA-DSB UPI product definition,
        which makes their issue a classification boundary problem rather than an
        ordinary completeness problem. The classification frontier panel therefore
        deserves separate treatment: the highlighted trades are
        {html.escape(frontier_use_cases)}, executed on
        {html.escape(frontier_platforms or 'the reported event-contract platforms')}.
        The regime outcome count is {html.escape(event_status_text)}. Kalshi-style
        regulated event contracts are treated as conditional under the CFTC logic,
        while Polymarket/offshore or EMIR-facing event contracts are outside the
        reporting chain used in this project. The additional test set still includes
        clean controls ({html.escape(', '.join(fully_compliant_controls) or 'none')})
        and designed data exceptions
        ({html.escape(', '.join(designed_exceptions) or 'none')}), so the dashboard
        separates three concepts that should not be mixed: valid trades, data
        reporting failures, and true taxonomy gaps.
      </p>
    </section>
    """


